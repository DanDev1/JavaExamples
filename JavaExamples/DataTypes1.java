package knowingjava;

public class DataTypes1 {

	public static void main(String[] args) {
		int aInteger = 2147483647;
		short aShort = 32767;
		float aFloat = 11111111;
		double aDouble = 12341234.123f;
		long aLong = 1234123412341234123L;
		boolean aBoolean = true;
		char aChar = 'A';
		
		System.out.println("This an Integer datatype : " +aInteger);
		System.out.println("This a Short datatype : " +aShort);
		System.out.println("This a Float datatype : " +aFloat);
		System.out.println("This a Double datatype : " +aDouble);
		System.out.println("This a Long datatype : " +aLong);
		System.out.println("This a Boolean datatype : " +aBoolean);
		System.out.println("This a Char datatype : " +aChar);
	}

}
